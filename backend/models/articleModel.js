class Article{
    constructor(id, title, author, body, images, created_at, updated_at){
        this.id = id;
        this.title = title;
        this.author = author;
        this.body = body;
        this.images = images;
        this.created_at = created_at;
        this.updated_at = updated_at;
    }
}

export default Article
